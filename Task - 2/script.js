// circle
let pi = 3.1416;
let r = Number(prompt("Enter R value: "));

let areaOfCircle = pi * r ** 2;
document.write("Area of circle: " + areaOfCircle + "<br>");

// area of square

let a = Number(prompt("Enter value of A: "));

areaOfSquare = a ** 2;
document.write("area of square: " + areaOfSquare + "<br>");

// rectangle

let height1 = Number(prompt("Enter the length: "));
let width = Number(prompt("Enter the width: "));

let areaOfRectangle = width * height1;
document.write("Area of ractangle: " + areaOfRectangle + "<br>");

// triangle

let base = Number(prompt("Enter the base: "));
let height = Number(prompt("Enter the height: "));

let areaOfTriangle = 0.5 * base * height;
document.write("Area of triangle: " + areaOfTriangle + "<br>");
